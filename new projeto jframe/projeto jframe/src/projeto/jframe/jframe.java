    private void datanascKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_datanascKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            // verificando a data
            Date dataNascimento = null;
            SimpleDateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
            formatoData.setLenient(false);
            String datas = (datanasc.getText());
            try {
                dataNascimento = formatoData.parse(datas);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Inválida!");
            }

            // cálculo da data
            Calendar hoje = Calendar.getInstance();
            hoje.getTime();

            Calendar dataNasc = Calendar.getInstance();
            dataNasc.setTime(dataNascimento);

            int idade = hoje.get(Calendar.YEAR) - dataNasc.get(Calendar.YEAR);

            if (hoje.get(Calendar.MONTH) < dataNasc.get(Calendar.MONTH)) {
                idade--;
            }

            // cálculo do responsável
            if (idade < 18) {
                labelrespo.setVisible(true);
                labelrespo1.setVisible(true);
                nomerespons.setVisible(true);
                nomerespons.requestFocus();
            } else {
                labelrespo.setVisible(false);
                labelrespo1.setVisible(false);
                nomerespons.setVisible(false);
                ti.requestFocus();
                ti.setSelected(true);
            }
        }
    }//GEN-LAST:event_datanascKeyPressed

