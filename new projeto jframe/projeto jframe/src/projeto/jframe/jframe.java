    private void salvarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_salvarKeyPressed
        // mudando o painel
        visupage.setVisible(true);
        cadpage.setVisible(false);

        // tranferindo os dados de um painel para outro
        // nome:
        labelnomev1.setText(nome.getText());

        // curso
        if (ti.isSelected()) {
            labelcursov1.setText(ti.getText());
        } else if (eletro.isSelected()) {
            labelcursov1.setText(eletro.getText());
        }

        //serie
        if (primeiro.isSelected()) {
            labelseriev1.setText(primeiro.getText());
        } else if (segundo.isSelected()) {
            labelseriev1.setText(segundo.getText());
        } else if (terceiro.isSelected()) {
            labelseriev1.setText(terceiro.getText());
        } else if (quarto.isSelected()) {
            labelseriev1.setText(quarto.getText());
        }

        // data dde nascimento
        labeldatav1.setText(datanasc.getText());
        sair.requestFocus();
    }//GEN-LAST:event_salvarKeyPressed

